package com.example.appspavi.gym;



public class Hero {
    String date, in,out;

    public Hero(String date,String in,String out) {
        this.date = date;
        this.in = in;
        this.out = out;

    }

    public String getDate() {
        return date;
    }
    public String getIn() {
        return in;
    }
    public String getOut() {
        return out;
    }


}
